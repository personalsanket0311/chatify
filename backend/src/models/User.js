import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
    fullName :{
        type: String,
        required: true,
    },
    email:{
        type: String,
        required: true,
        unique: true,
    },
    password:{
        type: String,
        unique: true,
        minlength:6,
    },
    profilePic:{
        type: String,
        default:""

    }
},{timestamps:true} // createdAt , updatedAt
);

const User = mongoose.model("User",userSchema);

export default User;